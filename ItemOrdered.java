class ItemOrdered
{
    private Item item;
    private int quantity;
    // Constructor
    public ItemOrdered(Item item, int quantity)
    {
        item=this.item;
        quantity=this.quantity;
    }
    //Σύνηθεις Getter Methods
    public Item getItem()
    {return item;}
    public int getQuantity()
    {return quantity;}
}